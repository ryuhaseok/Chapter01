package com.javaex.ex01;

public class Ex09 {

	public static void main(String [] args) {
		
		//대입연산자
		int a = 7;
		int b = 2;
		
		//산술연산자
		//int var01 = a+b;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b); 
		System.out.println(a%b);
		
		System.out.println("------------------------------------");
		System.out.println(7/2);
		System.out.println(7.0/2);
		System.out.println(7%2);
		System.out.println(7.0%2);

		System.out.println("------------------------------------");
		int var = -3;
		int pVar = +var;
		int mVar = -var;
		
		System.out.println(pVar);
		System.out.println(mVar);
		
		
	}
}
